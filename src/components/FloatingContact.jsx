const CloseIcon = () => (
  <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
    <line x1="18" y1="6" x2="6" y2="18" />
    <line x1="6" y1="6" x2="18" y2="18" />
  </svg>
);

export default function FloatingContact() {
  const [open, setOpen] = useState(false);
  const [copied, setCopied] = useState(false);
  const wrapperRef = useRef(null);
  const { pathname } = useLocation();

  // Only show on public pages
  const isVisible = PUBLIC_PATHS.includes(pathname);

  // Close on outside click
  useEffect(() => {
    const handler = (e) => {
      if (wrapperRef.current && !wrapperRef.current.contains(e.target)) {
        setOpen(false);
      }
    };
    document.addEventListener('mousedown', handler);
    return () => document.removeEventListener('mousedown', handler);
  }, []);

  if (!isVisible) return null;

  const handleCopyDiscord = async () => {
    try {
      await navigator.clipboard.writeText(DISCORD_USERNAME);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // Fallback for older browsers
      const textarea = document.createElement('textarea');
      textarea.value = DISCORD_USERNAME;
      document.body.appendChild(textarea);
      textarea.select();
      document.execCommand('copy');
      document.body.removeChild(textarea);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    }
  };

  return (
    <div className={styles.wrapper} ref={wrapperRef}>
      {/* Contact options */}
      <div className={`${styles.options} ${open ? styles.optionsOpen : ''}`}>
        <button
          className={`${styles.option} ${styles.discord} ${copied ? styles.copied : ''}`}
          onClick={handleCopyDiscord}
          title={copied ? 'Copied!' : `Copy Discord username: ${DISCORD_USERNAME}`}
        >
          <DiscordIcon />
          <span>{copied ? 'Copied!' : `Discord: ${DISCORD_USERNAME}`}</span>
          {copied && <CheckIcon />}
        </button>
        <a
          href="https://www.facebook.com/profile.php?id=61576467761976"
          target="_blank"
          rel="noopener noreferrer"
          className={`${styles.option} ${styles.facebook}`}
          title="Contact on Facebook"
        >
          <FacebookIcon />
          <span>Facebook</span>
        </a>
      </div>

      {/* Main toggle button */}
      <button
        className={`${styles.fab} ${open ? styles.fabOpen : ''}`}
        onClick={() => setOpen(!open)}
        aria-label={open ? 'Close contact menu' : 'Open contact menu'}
        id="floating-contact-btn"
      >
        {open ? <CloseIcon /> : <ChatIcon />}
      </button>
    </div>
  );
}

